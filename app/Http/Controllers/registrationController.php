     * Store a newly created resource in storage.
     *
     * @param  \Illuminate\Http\Request  $request
     * @return \Illuminate\Http\Response
     */
    public function store(Request $request)
    {
        $this->validate($request, [

            'email'=>'required|email',
            'phone'=>'required',
            'pass'=>'required',
            'confirmpass'=>'required',
            'bloodgroup'=>'required'

        ]);


        $candidates = new registration;
        $candidates->email=$request->input('email');
        $candidates->phone=$request->input('phone');
        $candidates->pass=$request->input('pass');
        $candidates->confirmpass=$request->input('confirmpass');
        $candidates->bloodgroup=$request->input('bloodgroup');
        $candidates->save();
        return redirect('/login');
    }

